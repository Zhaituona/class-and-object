public class MathMethod {
    public static void main(String[] args) {
        System.out.println(Math.abs(-2.36));//Math method can abs this is can show abselute number . if we put -2.34 , it will
       // show 2.36, if we put 2.34 it will show 2.34;
        System.out.println(Math.ceil(6.3));// this method ceil will show the upper number ,we put in to the parameters .//
        System.out.println(Math.floor(8.5));//this method floor will show the down number , we put into the parameters.it can
        //take only one parameter.//
        System.out.println(Math.max(6.3,9.6));// this method max can show the bigger one in the parameters , it can show two parameters//
        System.out.println(Math.min(4.86,6.9));// this method min can show the smaller one in  the parameters , it will
        //take two parameters .
        System.out.println(Math.pow(2,4));// this method pow can take two parameters , can calculate the time of first parameters ,
        // it will be 2 *2 * 2 *2 = 16;//
        System.out.println(Math.sqrt(9));// this can take the squrt , it will be 3;//
    }


}
